    public void loadToCbo() {
        try {
            String sql = "select distinct MaNV from NhanVien";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            DefaultComboBoxModel model = (DefaultComboBoxModel) cboNhanVien.getModel();
            model.removeAllElements();
            while (rs.next()) {
                String manv = rs.getString(1);
                model.addElement(manv);
            }
            st.close();
            rs.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi load Data To comboboxLSP");
        }
    }

    void insertToCTHD() {
        try {
            String maHD = lblMaHD.getText();
            for (int i = 0; i < pnlChiTietBan.ctb.listCTHD.size(); i++) {
                CTHD ct = pnlChiTietBan.ctb.listCTHD.get(i);
                String sql = "INSERT INTO CT_HoaDon(MaHD, MaSP, Soluong, ThanhTien) VALUES(?,?,?,?);";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, maHD);
                ps.setString(2, ct.getMaSP());
                ps.setInt(3, ct.getSoLuong());
                ps.setFloat(4, ct.getThanhTien());

                ps.executeUpdate();
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi thêm CTHD!!!");
        }
    }

    void insertToHD() {
        try {
            String mahd = lblMaHD.getText();
            String manv = (String) cboNhanVien.getSelectedItem();
            int maban = pnlChiTietBan.ctb.maban;
            int tongmon = Integer.parseInt(lblTongMon.getText());
            float tongtien = Float.parseFloat(lblTongTien.getText());

            String sql = "INSERT INTO HoaDon(MaHD, MaNV, MaBan, TongMon, TongTien) VALUES(?,?,?,?,?);";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, mahd);
            ps.setString(2, manv);
            ps.setInt(3, maban);
            ps.setInt(4, maban);
            ps.setFloat(5, tongtien);

            ps.executeUpdate();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Lỗi thêm HD!!!");
        }
    }

