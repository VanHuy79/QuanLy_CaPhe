    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        // TODO add your handling code here:
        try {
            int hoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn thanh toán hóa đơn này không?", "Thông Báo", JOptionPane.YES_NO_OPTION);
            if (hoi != JOptionPane.YES_OPTION) {
                return;
            }
            insertToHD();
            insertToCTHD();

            pnlChiTietBan.ctb.listCTHD.removeAll(pnlChiTietBan.ctb.listCTHD);
            JOptionPane.showMessageDialog(this, "Thanh toán thành công!");
            pnlChiTietBan.ctb.btnThem.setEnabled(false);
            pnlChiTietBan.ctb.btnSua.setEnabled(false);
            pnlChiTietBan.ctb.btnThanhToan.setEnabled(false);
            pnlChiTietBan.ctb.btnMoBan.setEnabled(true);
            pnlChiTietBan.ctb.trangthai = "Trống";
            pnlChiTietBan.ctb.lblMaHD.setText("...");
            pnlChiTietBan.ctb.lblTrangThai.setText(pnlChiTietBan.ctb.trangthai);
            pnlChiTietBan.ctb.updateTrangThai(pnlChiTietBan.ctb.trangthai);
            pnlChiTietBan.ctb.tblmodel.setRowCount(0);
            pnlChiTietBan.ctb.lblTongMon.setText("0");
            pnlChiTietBan.ctb.lblTongTien.setText("0");
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi thanh toán!!!");
        }
    }//GEN-LAST:event_btnDoneActionPerformed

